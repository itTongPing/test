package com.aukey.report.domain;

public class Supplier {
	//供应商ID
	private String supplierId;
	//供应商名称
	private String supplierName;
	//供应商类别
	private String type;
	//供应商营业状态
	private String commit_code;
	//供应商执照编码
	private String licenceCode;
	public String getSupplierId() {
		return supplierId;
	}
	public void setSupplierId(String supplierId) {
		this.supplierId = supplierId;
	}
	
	public String getSupplierName() {
		return supplierName;
	}
	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCommit_code() {
		return commit_code;
	}
	public void setCommit_code(String commit_code) {
		this.commit_code = commit_code;
	}
	public String getLicenceCode() {
		return licenceCode;
	}
	public void setLicenceCode(String licenceCode) {
		this.licenceCode = licenceCode;
	}
	public Supplier() {
		super();
	}
	public Supplier(String supplierId, String supplierName, String type, String commit_code, String licenceCode) {
		super();
		this.supplierId = supplierId;
		this.supplierName = supplierName;
		this.type = type;
		this.commit_code = commit_code;
		this.licenceCode = licenceCode;
	}
	

}
