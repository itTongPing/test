  KEY `warehouse_id_name` (`warehouse_id`),
  KEY `purchase_number_index` (`purchase_number`) USING BTREE,
  KEY `quality_inspection_number_index` (`quality_inspection_number`) USING BTREE,
  KEY `create_date_index` (`create_date`) USING BTREE,
  KEY `sku_code_inedx` (`sku_code`) USING BTREE,
