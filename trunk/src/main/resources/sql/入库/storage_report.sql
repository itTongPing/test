  KEY `muWarehouseName_index` (`muWarehouseName`) USING BTREE,
  KEY `corporation_id_index` (`corporation_id`) USING BTREE,
  KEY `transport_type_index` (`transport_type`) USING BTREE,
  KEY `is_tax_index` (`is_tax`) USING BTREE,
  KEY `stock_id_index` (`stock_id`) USING BTREE,
  KEY `saleName_index` (`saleName`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
