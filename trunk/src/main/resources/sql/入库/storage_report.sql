  `storage_number` varchar(50) NOT NULL COMMENT '入库单号',
  `quality_inspection_number` varchar(50) DEFAULT NULL COMMENT '质检单号',
  `purchase_number` varchar(50) DEFAULT NULL COMMENT '采购单号',
  `supplier_id` int(11) DEFAULT NULL,
  `Inspector` varchar(45) DEFAULT NULL COMMENT '质检员Id',
  `warehouse_id` mediumint(6) DEFAULT NULL COMMENT '仓库名称Id',
  `muWarehouseName` varchar(50) DEFAULT NULL COMMENT '目标仓库',
  `nondefective_number` int(11) DEFAULT NULL COMMENT '入库良品数',
  `rejects_number` int(11) DEFAULT NULL COMMENT '不良品数',
  `spare_parts_number` int(11) DEFAULT NULL,
  `storage_location_code` varchar(50) DEFAULT NULL,
  `create_user` varchar(45) DEFAULT NULL COMMENT '入库员Id',
  `create_date` datetime DEFAULT NULL COMMENT '入库时间',
  `sku_code` varchar(50) DEFAULT NULL COMMENT 'SKU',
  `SKUname` varchar(500) DEFAULT NULL,
  `type` varchar(10) DEFAULT NULL COMMENT '数据来源',
  `corporation_id` varchar(50) DEFAULT NULL COMMENT '法人主体id',
  `corporation_name` varchar(100) DEFAULT NULL COMMENT '法人主体',
  `transport_type` varchar(10) DEFAULT NULL,
  `transport_type_name` varchar(50) DEFAULT NULL COMMENT '运输方式',
  `is_tax` varchar(10) DEFAULT NULL,
  `is_tax_name` varchar(10) DEFAULT NULL COMMENT '是否退税',
  `reqUserName` varchar(255) DEFAULT NULL COMMENT '采购人员',
