CREATE TABLE stock_report_intermediate (
  id varchar(36) NOT NULL COMMENT '递增主健',
  legaler int(11) DEFAULT NULL COMMENT '法人主体',
  legaler_name varchar(128) DEFAULT NULL,
  department int(11) DEFAULT NULL COMMENT '需求部门',
  department_name varchar(64) DEFAULT NULL,
  country int(11) DEFAULT NULL COMMENT '国家',
  country_name varchar(64) DEFAULT NULL,
  sku varchar(64) DEFAULT NULL COMMENT 'sku',
  sku_name varchar(256) DEFAULT 'N/A' COMMENT 'sku名称',
  category int(11) DEFAULT NULL COMMENT '品类',
  category_name varchar(128) DEFAULT NULL,
  inventory int(11) DEFAULT NULL,
  inventory_name varchar(128) DEFAULT NULL COMMENT '仓库名称',
  count_in int(11) DEFAULT '0' COMMENT '入库数量',
  count_out int(11) DEFAULT '0' COMMENT '出库数量',
  usage_inventory int(11) DEFAULT '0' COMMENT '占用库存',
  way_inventory int(11) DEFAULT '0' COMMENT '调拔在途库存',
  available_inventory int(11) DEFAULT '0' COMMENT '可用库存',
  actual_inventory int(11) DEFAULT '0' COMMENT '实际库存',
  sum_in decimal(16,2) DEFAULT NULL,
  sum_out decimal(16,2) DEFAULT NULL,
  sum decimal(16,2) DEFAULT NULL COMMENT '金额',
  include_tax varchar(4) DEFAULT NULL COMMENT '是否含税',
  total_area decimal(11,6) DEFAULT NULL COMMENT '总面积',
  transport_type varchar(64) DEFAULT NULL COMMENT '运输方式',
  PRIMARY KEY (id),
  KEY idx_legaler (legaler) USING BTREE,
  KEY idx_department (department) USING BTREE,
  KEY idx_sku (sku) USING BTREE,
  KEY idx_inventory_name (inventory_name) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;